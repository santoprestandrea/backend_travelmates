package com.santoprestandrea_s00007624.backend_travelmates.entity;

/**
 * DOCUMENT CATEGORIES
 *
 * Categorizes different types of documents shared in a trip.
 */
public enum DocumentCategory {

    /**
     * TICKET - Flight tickets, train tickets, event tickets
     */
    TICKET,

    /**
     * BOOKING - Hotel confirmations, restaurant reservations
     */
    BOOKING,

    /**
     * ID_DOCUMENT - Passport copies, ID cards, visa documents
     */
    ID_DOCUMENT,

    /**
     * INSURANCE - Travel insurance, health insurance documents
     */
    INSURANCE,

    /**
     * MAP - Maps, itineraries, directions
     */
    MAP,

    /**
     * RECEIPT - Payment receipts, expense receipts
     */
    RECEIPT,

    /**
     * OTHER - Miscellaneous documents
     */
    OTHER
}
