/**
 * ACTIVITY CATEGORIES
 *
 * Categorizes different types of activities during a trip.
 */
public enum ActivityCategory {

    /**
     * SIGHTSEEING - Museums, monuments, viewpoints, guided tours
     */
    SIGHTSEEING,

    /**
     * RESTAURANT - Dining, food experiences, wine tasting
     */
    RESTAURANT,

    /**
     * TRANSPORT - Flights, trains, buses, car rentals
     */
    TRANSPORT,

    /**
     * ACCOMMODATION - Hotel check-in/out, Airbnb stays
     */
    ACCOMMODATION,

    /**
     * ENTERTAINMENT - Concerts, shows, nightlife, events
     */
    ENTERTAINMENT,

    /**
     * OUTDOOR - Hiking, beach, sports, nature activities
     */
    OUTDOOR,

    /**
     * SHOPPING - Markets, malls, souvenir shopping
     */
    SHOPPING,

    /**
     * OTHER - Miscellaneous activities
     */
    OTHER
}
